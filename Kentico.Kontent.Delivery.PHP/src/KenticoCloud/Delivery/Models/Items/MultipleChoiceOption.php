namespace KenticoCloud\Delivery\Models\Items;

/**
 * Class MultipleChoiceOption.
 */
class MultipleChoiceOption
{
    /**
     * Gets the name of the multiple choice option.
     *
     * @var string
     */
    public $name = null;

    /**
     * Gets the codename of the multiple choice option.
     *
     * @var string
     */
    public $codename = null;
}
